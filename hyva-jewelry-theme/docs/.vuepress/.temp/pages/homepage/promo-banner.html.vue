<li>Go to <strong>Content -&gt; Homepage Configurator -&gt; Sections</strong>.</li>
<li>Click <strong>Add New Section</strong>.</li>
<li>Enter a <strong>Title</strong>, for example <code v-pre>Premium Gift Edit</code>.</li>
