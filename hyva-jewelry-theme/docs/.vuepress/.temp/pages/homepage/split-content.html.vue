<template><div><h1 id="split-content" tabindex="-1"><a class="header-anchor" href="#split-content"><span>Split Content</span></a></h1>
<p>The <strong>Split Content</strong> section shows an image on one side and text on the other side. Use it for a brand story, a featured collection, a service message, or a short jewellery promise.</p>
<h2 id="add-split-content" tabindex="-1"><a class="header-anchor" href="#add-split-content"><span>Add Split Content</span></a></h2>
<ol>
<li>Go to <strong>Content -&gt; Homepage Configurator -&gt; Sections</strong>.</li>
<li>Click <strong>Add New Section</strong>.</li>
<li>Enter a <strong>Title</strong>, for example <code v-pre>The Modern Heirloom Collection</code>.</li>
<li>Set <strong>Section Type</strong> to <strong>Split Content</strong>.</li>
<li>Set <strong>Enabled</strong> to <strong>Yes</strong>.</li>
<li>Use a <strong>Sort Order</strong> so it appears in the right place on the homepage.</li>
<li>Fill the <strong>Split Content Configuration</strong> fields.</li>
<li>Click <strong>Save</strong>.</li>
</ol>
