<template><div><h1 id="hero-banner" tabindex="-1"><a class="header-anchor" href="#hero-banner"><span>Hero Banner</span></a></h1>
<p>The <strong>Hero Banner</strong> is the large banner at the top of the homepage. Use it for your main collection, sale, festive offer, or brand message.</p>
<p>It can show one slide or many slides.</p>
<h2 id="add-a-hero-banner" tabindex="-1"><a class="header-anchor" href="#add-a-hero-banner"><span>Add a Hero Banner</span></a></h2>
<ol>
<li>Go to <strong>Content -&gt; Homepage Configurator -&gt; Sections</strong>.</li>
<li>Click <strong>Add New Section</strong>.</li>
<li>Enter a <strong>Title</strong>, for example <code v-pre>Hero Banner</code>.</li>
<li>Set <strong>Section Type</strong> to <strong>Hero Banner</strong>.</li>
<li>Set <strong>Enabled</strong> to <strong>Yes</strong>.</li>
<li>Use a low <strong>Sort Order</strong>, such as <code v-pre>10</code>, so it shows first.</li>
<li>Fill the <strong>Hero Banner Configuration</strong> fields.</li>
<li>Click <strong>Save</strong>.</li>
</ol>
