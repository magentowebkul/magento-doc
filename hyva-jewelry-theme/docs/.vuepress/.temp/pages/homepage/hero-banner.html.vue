<h2 id="good-practice" tabindex="-1"><a class="header-anchor" href="#good-practice"><span>Good practice</span></a></h2>
<ul>
<li>Use 1 to 3 slides. Too many slides can distract shoppers.</li>
<li>Keep the heading easy to read.</li>
<li>Do not place important text inside the image. Use the Heading and Description fields instead.</li>
<li>Check the banner on mobile after saving.</li>
<li>If the text is hard to read, use a darker image or change the text color.</li>
</ul>
